    public void isUserValid() {
        if ((jTextField1.toString().length() > 0)
                && (jTextField2.toString().length() > 0)
                && (jTextField3.toString().matches("\\b[A-Z0-9._%-]+@[A-Z0-9.-]+\\.[A-Z]{2,4}\\b"))
                && (jTextField4.toString().length() > 0)
                && (jComboBox1.getItemCount() > 0)) {
            jButton1.setEnabled(true);
        }
    }

